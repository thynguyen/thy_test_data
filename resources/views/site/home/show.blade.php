<script src="https://cdn.socket.io/4.6.1/socket.io.min.js"></script>
<script>
const socket = io('http://localhost:3001');
socket.on('updateData', (data) => {

    // console.log('Received data update:', JSON.stringify(data));
    const arrTradings = data;
    for(i in arrTradings) {
        const code = arrTradings[i].code;
        let price = arrTradings[i].price24H;
        let percent = arrTradings[i].percent24H;
        const up_down = arrTradings[i].up_down;
        let color;
        
        if(percent !== '') {

            // console.log('code = ' + code + ', price = ' + price + ', percent = ' + percent);

            if(up_down === 'down') {
                percent = '-' + percent;
                color = 'rgb(240, 74, 90)';
            } else {
                percent = '+' + percent;
                color = 'rgb(40, 186, 152)';
            }                      

            $('#item_' + code).find('.update-price span').html(price);
            $('#item_' + code).find('.update-percent').html(percent);

            $('#item_' + code).find('.update-price').css({color: color});
            $('#item_' + code).find('.update-percent').css({'background-color': color});
        }
    }

});
</script>
